import 'package:analyzer/error/error.dart' as analyzer;
import 'package:analyzer/error/listener.dart';
import 'package:custom_lint_builder/custom_lint_builder.dart';
import 'package:talawa_lint/talawa_query_result/talawa_query_result_visitor.dart';

/// Lint rule that enforces checking `hasException` on QueryResult objects
/// before accessing the `data` property.
class TalawaQueryResultLintRule extends DartLintRule {
  const TalawaQueryResultLintRule() : super(code: _code);

  /// Metadata about the warning that will show-up in the IDE.
  static const _code = LintCode(
    name: 'talawa_query_result_check',
    problemMessage:
        'QueryResult.data accessed without checking hasException first.',
    correctionMessage:
        'Add an if (result.hasException) check before accessing data.',
    url: 'https://docs.talawa.io/docs/developers/talawa/talawa-lint/',
    errorSeverity: analyzer.ErrorSeverity.WARNING,
  );

  @override
  void run(
    CustomLintResolver resolver,
    ErrorReporter reporter,
    CustomLintContext context,
  ) {
    // Skip test files - match files in test directory ending with _test.dart
    final filePath = reporter.source.fullName;
    if (_isTestFile(filePath) || _isGeneratedFile(filePath)) {
      return;
    }

    final visitor = TalawaQueryResultVisitor(
      this,
      reporter,
    );

    // Register for method invocations to detect QueryResult usage
    context.registry.addMethodDeclaration((node) {
      node.accept(visitor);
    });

    context.registry.addFunctionDeclaration((node) {
      node.accept(visitor);
    });
  }

  /// Check if the file is a test file.
  /// Matches files ending with _test.dart in a test directory.
  /// Handles both Unix (/) and Windows (\) path separators.
  bool _isTestFile(String filePath) {
    // Normalize path separators for cross-platform compatibility
    final normalizedPath = filePath.replaceAll('\\', '/');
    // Match paths like /test/..._test.dart or /test/.../*_test.dart
    return normalizedPath.contains('/test/') &&
        normalizedPath.endsWith('_test.dart');
  }

  /// Check if the file is a generated file.
  /// Matches files ending with .g.dart (generated by code generators like json_serializable)
  /// or .mocks.dart (generated by mockito).
  bool _isGeneratedFile(String filePath) {
    return filePath.endsWith('.g.dart') || filePath.endsWith('.mocks.dart');
  }

  // Test-only methods - exposed for unit testing
  // ignore: public_member_api_docs
  bool isTestFileForTesting(String filePath) => _isTestFile(filePath);
  // ignore: public_member_api_docs
  bool isGeneratedFileForTesting(String filePath) => _isGeneratedFile(filePath);
}
